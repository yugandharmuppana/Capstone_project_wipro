package Control_Statement_Practice;

public class While_demo_aski {

	public static void main(String[] args) {
		char ch='A';//Declaration and initialization// stating point
		while(ch<='Z')// check condition// ending point
		{
			System.out.println(ch+"="+(int)ch);// Statement for execution
			ch++;// increment /decrement
		}
	}
}

	